abstract class PathResolverRecyclerAdapter<T>(private val mContext: Context) :
    RecyclerView.Adapter<PathResolverRecyclerAdapter.Holder<T?>>() {
    private val mList: MutableList<PathResolverRecyclerAdapter.Index<T?>> = ArrayList()
    private var mClickListener: PathResolverRecyclerAdapter.OnClickListener<T?>? = null

    /*
     * To fix issues with the RecyclerView not appearing, the first item must be provided
     * when dealing with wrap_content height.
     */
    abstract fun onFirstItem(): PathResolverRecyclerAdapter.Index<T?>
    override fun onCreateViewHolder(parent: ViewGroup, viewType: Int): PathResolverRecyclerAdapter.Holder<T?> {
        return PathResolverRecyclerAdapter.Holder(
            LayoutInflater.from(parent.context).inflate(R.layout.list_pathresolver, null)
        )
    }

    override fun onBindViewHolder(holder: PathResolverRecyclerAdapter.Holder<T?>, position: Int) {
        holder.index = getList()[position]
        holder.text.text = holder.index.title
        holder.image.setImageResource(holder.index.imgRes)
        holder.image.visibility = if (position == 0) View.GONE else View.VISIBLE
        holder.text.isEnabled = itemCount - 1 != position
        if (mClickListener != null) holder.text.setOnClickListener { view: View? -> mClickListener.onClick(holder) }
    }

    fun initAdapter() {
        synchronized(mList) {
            mList.clear()
            mList.add(onFirstItem())
        }
    }

    fun getContext(): Context {
        return mContext
    }

    override fun getItemCount(): Int {
        return mList.size
    }

    fun getList(): List<PathResolverRecyclerAdapter.Index<T?>> {
        return mList
    }

    fun setOnClickListener(clickListener: PathResolverRecyclerAdapter.OnClickListener<T?>?) {
        mClickListener = clickListener
    }

    interface OnClickListener<E> {
        fun onClick(holder: PathResolverRecyclerAdapter.Holder<E>?)
    }

    class Holder<E> private constructor(var container: View) : RecyclerView.ViewHolder(container) {
        var image: ImageView
        var text: Button
        var index: PathResolverRecyclerAdapter.Index<E>? = null

        init {
            image = container.findViewById(R.id.list_pathresolver_image)
            text = container.findViewById(R.id.list_pathresolver_text)
        }
    }

    class Index<D>(var title: String?, var imgRes: Int, var `object`: D) {
        constructor(title: String?, `object`: D) : this(
            title,
            R.drawable.ic_keyboard_arrow_right_white_24dp,
            `object`
        ) {
        }
    }

    init {
        initAdapter()
    }
}